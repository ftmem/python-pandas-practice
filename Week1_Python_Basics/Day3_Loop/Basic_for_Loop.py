#Print numbers from 1 to 10
for i in range(1, 11):  #range(1, 11) start at 1, stops before 11
    print(i)

#Print numbers from 0 to 10
for i in range(10):  #range(10) start from 0
    print(i)

#Print numbers from 0 to 20, step by 2
for i in range(1, 21, 2):   #renge(0, 21, 2) start = 0, stop = 21, step = 2
    print(i)




